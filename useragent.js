function c(needle,haystack){
	haystack = (haystack?haystack:navigator.userAgent);
	switch(typeof needle){
		case 'string':
			return haystack.indexOf(needle) > -1;
			break;
		case 'object':
			var f=false;
			for(var i=0; i<needle.length; i++){
				if(haystack.indexOf(needle[i]) > -1) f=true;
			}
			return f;
			break;
	}
}

